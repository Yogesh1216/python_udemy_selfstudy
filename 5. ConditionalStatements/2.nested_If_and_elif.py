# who is eldest among 3 persons:
yogesh = int(input('what is yogesh age: '))
rohit = int(input('what is rohit age: '))
rahul = int(input('what is rahul age: '))
if yogesh>=rohit and yogesh>=rahul:
    print('yogesh is elder')
elif rohit>rahul:
    print('rohit is elder')
else:
    print('rahul is elder')

